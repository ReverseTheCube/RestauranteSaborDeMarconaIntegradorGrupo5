/*
 * Este script maneja la navegación principal del menú de platos.
 */

function crear() {
    // Redirige a la página de crear
    window.location.href = 'menu-crear.html';
}

function editar() {
    // Redirige a la página de editar
    window.location.href = 'menu-editar.html';
}

function eliminar() {
    // Redirige a la página de eliminar
    window.location.href = 'menu-eliminar.html';
}

function atras() {
    // Vuelve a la página principal del administrador
    window.location.href = 'admin.html';
}